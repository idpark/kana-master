// Hiragana↔Katakana pair lookup
const _pairMap=new Map();
H.forEach((h,i)=>{if(K[i])_pairMap.set(h.c,K[i].c);_pairMap.set(K[i].c,h.c)});
export function getPair(char){return _pairMap.get(char)||null}
