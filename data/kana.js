function makeVariants(base,map,romMap,type){
  return base.filter(b=>map[b.c]).map(b=>({c:map[b.c],r:romMap[b.r]||b.r,g:b.g,h:b.h,s:b.s,m:b.m,isV:true,vt:type,bc:b.c,br:b.r}));
}
function makeYouon(base,isK){
  const combos=isK?[
    ["キ","k"],["シ","sh"],["チ","ch"],["ニ","ny"],["ヒ","hy"],["ミ","my"],["リ","ry"],
    ["ギ","gy"],["ジ","j"],["ビ","by"],["ピ","py"]
  ]:[
    ["き","k"],["し","sh"],["ち","ch"],["に","ny"],["ひ","hy"],["み","my"],["り","ry"],
    ["ぎ","gy"],["じ","j"],["び","by"],["ぴ","py"]
  ];
  const ya=isK?"ャ":"ゃ",yu=isK?"ュ":"ゅ",yo=isK?"ョ":"ょ";
  const res=[];
  combos.forEach(([bc,br])=>{
    [{s:ya,k:"ya"},{s:yu,k:"yu"},{s:yo,k:"yo"}].forEach(({s:sf,k:sk})=>{
      let rom=br==="sh"?{ya:"sha",yu:"shu",yo:"sho"}[sk]:br==="ch"?{ya:"cha",yu:"chu",yo:"cho"}[sk]:br==="j"?{ya:"ja",yu:"ju",yo:"jo"}[sk]:br+sk;
      const allV=[...makeVariants(base,isK?DK_MAP_K:DK_MAP_H,ROM_DK,"탁음"),...makeVariants(base,isK?HD_MAP_K:HD_MAP_H,ROM_HD,"반탁음")];
      const info=base.find(x=>x.c===bc)||allV.find(x=>x.c===bc);
      if(info) res.push({c:bc+sf,r:rom,g:info.g,h:info.h||"",s:info.s||"",m:info.m||"",isV:true,vt:"요음",bc,br:info.r||"",sd:`${bc}+${sf}=${bc+sf} (${rom})`});
    });
  });
  return res;
}

